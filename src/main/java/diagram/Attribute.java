package diagram;

public class Attribute {
	private String type = "";
	private String name = "";
	
	public String getType() {
		return type;
	}
	public void setType(String pType) {
		type = pType;
	}
	public String getName() {
		return name;
	}
	public void setName(String pName) {
		name = pName;
	}
}
